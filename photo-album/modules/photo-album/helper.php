class Helper
{
    function __construct()
    {
        //
    }

    function getWeekdayVi(int $time) {
        date_default_timezone_set('Asia/Ho_Chi_Minh');
        $date = getdate($time);
        $weekday = strtolower($date['weekday']);
        switch($weekday) {
            case 'monday':
                $weekday = 'Thứ hai';
                break;
            case 'tuesday':
                $weekday = 'Thứ ba';
                break;
            case 'wednesday':
                $weekday = 'Thứ tư';
                break;
            case 'thursday':
                $weekday = 'Thứ năm';
                break;
            case 'friday':
                $weekday = 'Thứ sáu';
                break;
            case 'saturday':
                $weekday = 'Thứ bảy';
                break;
            default:
                $weekday = 'Chủ nhật';
                break;
        }
        return $weekday.' - '.date('d/m/Y H:i:s', $time);
    }
}
